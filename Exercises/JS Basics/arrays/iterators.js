/*
for ... of

The for ... of statement creates a loop iterating over iterable objects.
*/

// Use a for of loop to iterate over simpleArray

const simpleArray = [1, 2, 3, 4, 5];

for (const n of simpleArray) {
    console.log(n);
}

// Use a for of loop to iterate over nums

const nums = [2, 7, 11, 15];

for (const n of nums) {
    console.log(n);
}